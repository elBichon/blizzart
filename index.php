	  <h1>L'école</h1>
	  <h4>Le mot du directeur</h4>
	  <h3>Télécom SudParis, Ecole d’ingénieurs du numérique, une réponse aux enjeux des entreprises</h3>
	  <p><strong>Télécom SudParis</strong>, créée en 1979 sous le
	    nom d’Institut national des télécommunications (INT), est
	    une <strong>Grande école de l’<a id="c7111" href="http://www.mines-telecom.fr/" target="_blank" title="nouvelle fenêtre">Institut Mines-Télécom</a></strong>. Notre ambition est de « former des femmes et des hommes de toutes origines, <strong>bâtisseurs de la société numérique, et de contribuer à sa construction par un enseignement et une recherche d’excellence</strong>, sources d’innovation et de valeur économique et sociale ».</p>
	  <p><strong><em><br />Force singulière : un partenariat unique avec une école de management</em></strong></p>
	  <p><strong>Télécom SudParis partage son campus situé à proximité de Paris avec Télécom Ecole de Management.</strong> La symbiose entre les deux écoles, sans équivalent en France, a contribué à forger l’identité propre de chacune. Elle constitue une force singulière dans les multiples dimensions de leurs missions d’enseignement, de recherche et d’innovation ainsi qu’un atout majeur pour leurs diplômés.</p>
	  <p><em><strong><br />Proposer une offre de formation diversifiée et recherchée</strong></em></p>
	  <p><strong>Pionnière dans la pédagogie par projets et le développement de l’esprit entrepreneurial</strong>, Télécom SudParis forme :<a href="http://www.campus-channel.com/telecom-sudparis-programme-ingenieur-prepas.html" target="_blank" title="nouvelle fenêtre"></a></p>
	  <ul>
	    <li><p>des ingénieurs généralistes, </p></li>
	    <li><p>des ingénieurs de spécialité, </p></li><li><p>des cadres scientifiques </p></li>
	    <li><p>des docteurs.</p></li>
	  </ul>
	  <p>Télécom SudParis propose <strong>au travers de ses
	      programmes et de ses partenariats, une <a  href="http://www.telecom-sudparis.eu/p_fr_formations_ingenieur-generaliste_1041.html" target="_blank" title="nouvelle fenêtre">offre de formations</a> très diversifiée</strong>. En propre, Télécom SudParis propose de nombreuses programmes dans des domaines qui vont des mathématiques appliquées aux réseaux et aux systèmes d’information, en passant par l’électronique, l’image et le multimédia. <strong>Nous apportons une attention particulière au projet de chaque élève et au fait qu’il développe des compétences larges pour sa réussite professionnelle</strong>. Les voies d’approfondissement sont particulièrement reconnues et ouvrent sur des domaines porteurs. Par exemple, Télécom SudParis est la seule école dont le cursus permet de devenir expert en sécurité des systèmes d’information, certifié par l’Agence nationale de la sécurité des systèmes d’information (ANSSI). Ses partenariats ouvrent de nombreux autres horizons, au sein de l’Institut Mines-Télécom ou à l’international. </p>

	  <p><em><strong><br />Donner une longueur d’avance aux entreprises grâce à nos recherches</strong></em></p>

	  <p><strong>Télécom SudParis développe une <a href="http://www.telecom-sudparis.eu/p_fr_recherche_presentation_8307.html" target="_blank" title="nouvelle fenêtre">activité de recherche</a> de haut niveau</strong>, en partenariat avec des entreprises, répondant à <strong>des enjeux majeurs de la société contemporaine</strong>, tels que :</p>

	  <ul>
	    <li><p><strong>les industries et les services numériques</strong> : réseaux, cloud, cybersécurité, applications et contenus multimédias, réalité augmentée, jeux vidéos,</p></li>
	    <li><p><strong>l’énergie et les smarts grids</strong> : optimisation de l&#39;énergie, transparence optique, radar passif, interactions véhicules-grille, contrôle distribué,</p></li>
	    <li><p><strong>la ville numérique et les transports intelligents</strong> : énergie, analyse des déplacements, capteurs, optimisation, intelligence ambiante,</p></li>
	    <li class="clear"><p><strong>la santé et l’assistance à la personne</strong> : capteurs, fusion hétérogène, télévigilance, robotique d’accompagnement, traitement du signal, médecine personnalisée. </p></li>
	  </ul>
	  <p><em><strong><br />Adapter notre offre à l’international et à la création d’entreprise</strong></em></p>
	  <p><strong>Avec 105 accords de partenariats dans plus de 36 pays, Télécom SudParis est aussi résolument ouverte à l’international</strong>. Avec 9 autres grandes écoles, 2 universités et 7 organismes de recherche, <strong>Télécom SudParis est membre fondateur de l’<a href="https://www.universite-paris-saclay.fr/" target="_blank" title="nouvelle fenêtre">Université Paris-Saclay</a></strong>, institution qui se destine à être le plus grand campus scientifique et technique d’Europe.</p>
	  <p class="clear">Enfin, <strong>Télécom SudParis accompagne
	      plus de 15 créations d’entreprise par
	      an</strong>. Son <strong><a href="http://incubateur.telecom-sudparis.eu/fr/"
					  target="_blank" title="nouvelle fenêtre">incubateur</a></strong>, parmi les premiers de France organise le 2e concours d’entrepreneuriat de France, le Trophée Start-up numérique, pour identifier et soutenir des candidats franciliens.</p>
	  <p><em><strong><br />Télécom SudParis, pour aller plus loin</strong></em></p>
	  <p>Vous pouvez également suivre nos actualités sur le e-magazine <strong><a href="http://lesnews.telecom-sudparis.eu/" target="_blank" title="nouvelle fenêtre">les News de Télécom SudParis</a></strong>. Quel que soit le motif de votre visite sur notre site, j’espère que celui-ci répondra à vos attentes et vous convaincra de l’intérêt de nous rencontrer. Dans l’attente, je vous souhaite une agréable visite.</p>
